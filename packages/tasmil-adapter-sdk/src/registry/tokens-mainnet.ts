// ─── stellar.expert icon helper ─────────────────────────────────

const SE = "https://stellar.expert/explorer/public/asset";

function seIcon(code: string, issuer: string): string {
  return `${SE}/${code}-${issuer}/icon`;
}

// ─── Helper: stellar-only token ─────────────────────────────────

function stellarToken(
  symbol: string,
  name: string,
  contract: string,
  issuer: string | null,
  logo: string,
  swappableOn: string[] = DEX_SWAP,
  decimals = 7,
): CrossChainToken {
  return {
    symbol, name, logo, decimals,
    chains: ["stellar"],
    addresses: { stellar: contract },
    ...(issuer ? { issuer } : {}),
    bridgeable: false,
    bridgeableVia: [],
    swappableOn,
  };
}

