// ─── Typed accessors ──────────────────────────────────────────────

export function getContracts(network: StellarNetwork): NetworkContracts {
  return NETWORK_CONTRACTS[network];
}

export function getTokenContracts(network: StellarNetwork): TokenContracts {
  return NETWORK_CONTRACTS[network].tokens;
}

export function getBlendContracts(network: StellarNetwork): BlendContracts {
  return NETWORK_CONTRACTS[network].blend;
}

export function getSoroswapContracts(network: StellarNetwork): SoroswapContracts {
  return NETWORK_CONTRACTS[network].soroswap;
}

export function getAquariusContracts(network: StellarNetwork): AquariusContracts {
  return NETWORK_CONTRACTS[network].aquarius;
}

export function getPhoenixContracts(network: StellarNetwork): PhoenixContracts {
  return NETWORK_CONTRACTS[network].phoenix;
}

export function getDefindexContracts(network: StellarNetwork): DefindexContracts {
  return NETWORK_CONTRACTS[network].defindex;
}

