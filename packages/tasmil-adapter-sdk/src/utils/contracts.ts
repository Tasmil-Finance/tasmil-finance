/**
 * Stellar/Soroban contract addresses and known assets.
 *
 * Design principles:
 * - Each protocol has its own typed interface — no more generic `Record<string, unknown>` casts
 * - Separate concerns: protocol contracts, asset definitions, pool lists
 * - Network-keyed maps for easy per-network lookup
 *
 * Moved from apps/mcp-stellar/src/config/contracts.ts
 */

import type { StellarNetwork } from "../types/common.js";

// ─── Per-protocol contract interfaces ────────────────────────────

export interface BlendContracts {
  backstop: string;
  poolFactory: string;
  emitter: string;
  blndToken: string;
  usdcToken: string;
  cometLpPool: string;
  cometFactory: string;
  /** Known named pools (dynamic discovery via backstop rewardZone is preferred) */
  knownPools: ReadonlyArray<{ name: string; address: string }>;
}

export interface SoroswapContracts {
  router: string;
  factory?: string;
}

export interface AquariusContracts {
  router: string;
  aquaToken: string;
  /**
   * Testnet: Aquarius uses a different USDC contract than the canonical Circle testnet USDC.
   * When set, the Aquarius adapter substitutes this address for the network's default USDC.
   */
  usdcOverride?: string;
}

export interface PhoenixContracts {
  factory: string;
  multihop: string;
  phoToken: string;
  /** Pre-mapped pool addresses for gas-free discovery */
  knownPools: ReadonlyArray<{
    address: string;
    stake: string;
    tokens: [string, string];
  }>;
}

export interface DefindexContracts {
  factory: string;
  /**
   * Testnet: factory address is wrong (copy-paste from Blend).
   * Use these hardcoded vaults instead.
   */
  knownVaults?: ReadonlyArray<{ name: string; address: string; asset: string }>;
}

export interface TokenContracts {
  /** Native XLM SAC (standard on Soroban) */
  xlmSac: string;
  /** XLM SAC as used by Soroswap aggregator (different on mainnet) */
  xlmSacSoroswap: string;
  usdc: string;
}

